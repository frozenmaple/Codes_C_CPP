#include<stdio.h>
int main()
{
	float c = 85.95;
	double d = 85.95;
	printf("c=%f\n", c);
	printf("d=%lf\n", d);

	float a = 3.141592653589793;
	double b = 3.141592653589793;
	printf("a=%.15f\n", a);
	printf("b=%.15lf\n", b);
	return 0;
}
